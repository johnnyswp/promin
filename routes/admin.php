    Route::post('send-email-pro/{product_id}/', function(Request $req, $id){
        if(!$producto=Producto::find($id)){
            return view('404');
        }
        $gastos = Gasto::where('producto_id', $producto->id)->get();
        $accesos = DB::table('users')
                ->join('role_user', 'role_user.user_id', '=', 'users.id')     
                ->join('roles', 'role_user.role_id', '=', 'roles.id')
                ->where('roles.name','<>','cliente') 
                //->where('users.id','<>',Auth::user()->id)
                ->pluck('users.name', 'users.id')->toArray();
        $accesos['0'] = "Seleccione una opción";
        $bitacoras = Bitacora::where('producto_id', $producto->id)->get();
        $documentos = Documento::where('producto_id', $producto->id)->get();

        if($producto->mx==1){
            $pdf = PDF::loadView('admin.pdf.producto', ['documentos'=>$documentos, 'bitacoras'=>$bitacoras, 'producto'=>$producto,'gastos'=>$gastos]);
