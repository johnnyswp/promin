            Mail::to(explode(',', $req->mail))->send(new ProductoSend($producto, base_path('asset_admin/pdfs/'.$producto->nombre.'.pdf')));
            return "Exito";
        }else{
            return view('404');
        }
    });
    Route::post('add-galery', function(Request $req){
        if($req->hasFile('file')){
            $date = date_create();$name= date_format($date, 'U-').str_random(50);
            $file=$req->file('file');
            $file->move( base_path().'/asset/uploads' , $name.".".$file->getClientOriginalExtension());
            if(Imagen::where('producto_id', $req->id)->get()){
                $position = intval(Imagen::where('producto_id', $req->id)->max('order_img'))+1;
            }else{
                $position = 0;
            }
            $imagen = new Imagen;
            $imagen->producto_id = $req->id;
            $imagen->order_img = $position;
            $imagen->picture='/asset/uploads/'.$name.".".$file->getClientOriginalExtension();
            $imagen->save();

            return response()->json($imagen);
        }
    });
    Route::post('delete-galery', function(Request $req){
